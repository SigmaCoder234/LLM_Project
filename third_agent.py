def get_gigachat_token(auth_key):
    """
    –ü–æ–ª—É—á–∞–µ—Ç access_token –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å GigaChat API (–¥–µ–π—Å—Ç–≤—É–µ—Ç ~30 –º–∏–Ω—É—Ç).
    """
    url = "https://ngw.devices.sberbank.ru:9443/api/v2/oauth"
    headers = {
        "Authorization": f"Basic {auth_key}"
    }
    try:
        response = requests.post(url, headers=headers, timeout=10)
        response.raise_for_status()
        token = response.json().get("access_token", "")
        return token
    except Exception as e:
        print(f"[–û–®–ò–ë–ö–ê] –ù–µ —É–¥–∞–ª–æ—Å—å –ø–æ–ª—É—á–∏—Ç—å —Ç–æ–∫–µ–Ω GigaChat: {e}")
        return None

def check_message_with_gigachat(message, rules, prompt, token):
    """
    –û—Ç–ø—Ä–∞–≤–ª—è–µ—Ç –∑–∞–ø—Ä–æ—Å –≤ GigaChat API –¥–ª—è –∞–Ω–∞–ª–∏–∑–∞ —Å–æ–æ–±—â–µ–Ω–∏—è –ø–æ –ø—Ä–∞–≤–∏–ª–∞–º —á–∞—Ç–∞.
    –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Ç–µ–∫—Å—Ç–æ–≤—ã–π –≤—ã–≤–æ–¥ –Ω–µ–π—Ä–æ—Å–µ—Ç–∏.
    """
    url = "https://gigachat.devices.sberbank.ru/api/v1/chat/completions"
    rules_text = "\n".join(rules)
    system_msg = f"–ü—Ä–∞–≤–∏–ª–∞ —á–∞—Ç–∞:\n{rules_text}\n\n{prompt}"
    user_msg = f"–°–æ–æ–±—â–µ–Ω–∏–µ:\n{message}"
    
    headers = {
        "Authorization": f"Bearer {token}",
        "Content-Type": "application/json"
    }
    
    data = {
        "model": "GigaChat",
        "messages": [
            {"role": "system", "content": system_msg},
            {"role": "user", "content": user_msg}
        ],
        "temperature": 0.2,  # –ù–∏–∑–∫–∞—è —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞ –¥–ª—è —Å—Ç–∞–±–∏–ª—å–Ω–æ—Å—Ç–∏ —Ä–µ—à–µ–Ω–∏–π
        "max_tokens": 256
    }
    
    try:
        response = requests.post(url, headers=headers, json=data, timeout=30)
        response.raise_for_status()
        result = response.json()
        content = result["choices"][0]["message"]["content"]
        return content
    except Exception as e:
        error_msg = f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –∑–∞–ø—Ä–æ—Å–µ –∫ GigaChat: {e}"
        print(f"[–û–®–ò–ë–ö–ê] {error_msg}")
        return error_msg

def parse_gigachat_response(text):
    """
    –ü–∞—Ä—Å–∏—Ç –æ—Ç–≤–µ—Ç GigaChat –∏ –æ–ø—Ä–µ–¥–µ–ª—è–µ—Ç, –Ω—É–∂–µ–Ω –ª–∏ –±–∞–Ω.
    –ò—â–µ—Ç –∫–ª—é—á–µ–≤—ã–µ —Å–ª–æ–≤–∞ –≤ –æ—Ç–≤–µ—Ç–µ –Ω–µ–π—Ä–æ—Å–µ—Ç–∏.
    """
    text_lower = text.lower()
    
    # –ö–ª—é—á–µ–≤—ã–µ —Å–ª–æ–≤–∞ –¥–ª—è –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è –≤–µ—Ä–¥–∏–∫—Ç–∞
    ban_keywords = ["–≤–µ—Ä–¥–∏–∫—Ç: –¥–∞", "–Ω–∞—Ä—É—à–µ–Ω–∏–µ –æ–±–Ω–∞—Ä—É–∂–µ–Ω–æ", "–Ω–∞—Ä—É—à–∞–µ—Ç –ø—Ä–∞–≤–∏–ª–∞", "–∑–∞–±–∞–Ω–∏—Ç—å", "–±–ª–æ–∫–∏—Ä–æ–≤–∞—Ç—å"]
    no_ban_keywords = ["–≤–µ—Ä–¥–∏–∫—Ç: –Ω–µ—Ç", "–Ω–µ—Ç –Ω–∞—Ä—É—à–µ–Ω–∏–π", "–Ω–µ –Ω–∞—Ä—É—à–∞–µ—Ç", "–ø—Ä–∞–≤–∏–ª–∞ —Å–æ–±–ª—é–¥–µ–Ω—ã", "–Ω–∞—Ä—É—à–µ–Ω–∏–π –Ω–µ –Ω–∞–π–¥–µ–Ω–æ"]
    
    # –û–ø—Ä–µ–¥–µ–ª—è–µ–º –≤–µ—Ä–¥–∏–∫—Ç
    ban = False
    
    # –°–Ω–∞—á–∞–ª–∞ –ø—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞ –æ—Ç—Å—É—Ç—Å—Ç–≤–∏–µ –Ω–∞—Ä—É—à–µ–Ω–∏–π (–ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç)
    if any(word in text_lower for word in no_ban_keywords):
        ban = False
    # –ó–∞—Ç–µ–º –ø—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞ –Ω–∞–ª–∏—á–∏–µ –Ω–∞—Ä—É—à–µ–Ω–∏–π
    elif any(word in text_lower for word in ban_keywords):
        ban = True
    
    return {
        "ban": ban,
        "reason": text.strip()
    }

